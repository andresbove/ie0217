#include <iostream> // Se incluye la biblioteca 'iostream' en el header para imprimir mensajes en pantalla q
using namespace std; // 

int main () { // Se define la función principal
 for (int i = 1; i <= 5; i++) { // Se define un ciclo 'for'. Se crea y se define la variable de tipo entero 'i'
   // Siempre y cuando 'i' sea menor que 5, se le sumará +1 al valor de esta variable.
    cout << i << " "; // Se imprime un espacio vacío en cada iteración

 }
return 0;

}